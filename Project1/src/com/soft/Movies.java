package com.soft;

public class Movies {
	private String moviename;
	private int moveproduce;
	private String movietime;
	private int moviedate;

	public int getMoviedate() {
		return moviedate;
	}

	public void setMoviedate(int moviedate) {
		this.moviedate = moviedate;
	}

	public String getMoviename() {
		return moviename;
	}

	public void setMoviename(String moviename) {
		this.moviename = moviename;
	}

	public int getMoveproduce() {
		return moveproduce;
	}

	public void setMoveproduce(int moveproduce) {
		this.moveproduce = moveproduce;
	}

	public String getMovietime() {
		return movietime;
	}

	public void setMovietime(String movietime) {
		this.movietime = movietime;
	}
}
