package com.soft;

public class Category {
	private String foodname;
	private String foodproduct;
	private double foodprice;
	private String foodexpire;

	public String getFoodname() {
		return foodname;
	}

	public void setFoodname(String foodname) {
		this.foodname = foodname;
	}

	public String getFoodproduct() {
		return foodproduct;
	}

	public void setFoodproduct(String foodproduct) {
		this.foodproduct = foodproduct;
	}

	public double getFoodprice() {
		return foodprice;
	}

	public void setFoodprice(double foodprice) {
		this.foodprice = foodprice;
	}

	public String getFoodexpire() {
		return foodexpire;
	}

	public void setFoodexpire(String foodexpire) {
		this.foodexpire = foodexpire;
	}
}
